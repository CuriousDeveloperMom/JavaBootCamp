package com.company;

public class Patio {

    private double areaOfPatio;
    private boolean isWooden;

    //default constructor

    public Patio() {
    }


    //Getter And Setters


    public double getAreaOfPatio() {
        return areaOfPatio;
    }

    public void setAreaOfPatio(double areaOfPatio) {
        this.areaOfPatio = areaOfPatio;
    }

    public boolean isWooden() {
        return isWooden;
    }

    public void setWooden(boolean wooden) {
        isWooden = wooden;
    }

}
