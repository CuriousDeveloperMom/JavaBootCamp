package com.company;

public class Bedroom {

    private int numOfBedroom;
    private double areaOfBedroom;

    //default constructor

    public Bedroom() {
    }


    //Getter And Setters


    public int getNumOfBedroom() {
        return numOfBedroom;
    }

    public void setNumOfBedroom(int numOfBedroom) {
        this.numOfBedroom = numOfBedroom;
    }

    public double getAreaOfBedroom() {
        return areaOfBedroom;
    }

    public void setAreaOfBedroom(double areaOfBedroom) {
        this.areaOfBedroom = areaOfBedroom;
    }
}
